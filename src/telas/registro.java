 void atualizarTabela(){
    try {
        Conexaobd conexao = new Conexaobd();
        Funcionario atualizar = new Funcionario();
          
         jTabelaCadastro.setModel(new javax.swing.table.DefaultTableModel(
           new Object [][]{}, new String []{"ID","nome","sobrenome","email","senha"} ));
    DefaultTableModel tabela = (DefaultTableModel) jTabelaCadastro.getModel();
    while (atualizar.getConsulta().next()) {
    int cod = atualizar.getConsulta().getInt("id");
    String nome = atualizar.getConsulta().getString("nome");
    String sobrenome = atualizar.getConsulta().getString("sobrenome");
    String email = atualizar.getConsulta().getString("email");
    String senha = atualizar.getConsulta().getString("senha");

    //Date data = (Date)cliente.consulta.getDate("data");
    //String dataNormal = DT.format(data);
    tabela.addRow(new Object[]{cod,nome,sobrenome,email,senha} );
    }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Não foi possivel atualizar a tabela");
    }
 }
