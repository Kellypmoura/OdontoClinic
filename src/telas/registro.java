private void listarValores(){
        try {
          Funcionario funcionario = new Funcionario();
          DefaultTableModel model = (DefaultTableModel) jTabelaCadastro.getModel();
          model.setNumRows(0);
          ArrayList<Funcionario> lista = funcionario.ListarFuncionario();
          
          for(int num = 0 ; num < lista.size(); num++){
              model.addRow(new Object[]{
              lista.get(num).getNome(),
              lista.get(num).getSobrenome(),
              lista.get(num).getEmail(),
              lista.get(num).getSenha()
              });
          }
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null,"Listar Valores VIEW"+ ex);
        }
    }

